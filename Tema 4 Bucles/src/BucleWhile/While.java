package BucleWhile;

public class While {
    public static void main(String[] args) {
        int numeros = 500;
        while (numeros >= 0){
            System.out.println(numeros);
            numeros --;
        }

        int numeros2 = 0;
        while (numeros2 <= 500){
            System.out.println(numeros2);
            numeros2 ++;
        }
    }
}
