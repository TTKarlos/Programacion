package Primitivos;

public class Variables {
    public static void main(String[] args) {

        int entero= 13;
        System.out.println("Numero entero: " + entero);

        float decimal = 1.5f;
        System.out.println("Este es una variable de numero decimal: " + decimal);

        double decimalEnorme = 3.458255455456698774521231554f;
        System.out.println("Cuando una decimal es muy grande usamos un double: " + decimalEnorme);

        char caracter = 'a';
        System.out.println("Para definir un caracter o una letra sola usamos el char con comillas simples: " + caracter);

        boolean desicion = true;
        System.out.println("Los booleans se usan para tomar decisones de true o false: " + desicion);


    }

}
